#include "asm.h"

void		ft_parse_file(t_asm *env)
{
	int		i;

	i = 0;
	while (env->file_content[i])
	{
		if (ft_strchr(env->file_content[i], '.'))
		{
			ft_printf("parse\n");
		}
		i++;
	}
}